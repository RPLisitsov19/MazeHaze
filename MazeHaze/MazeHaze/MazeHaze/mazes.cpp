unsigned char player = '*';
int posX = 1, posY = 1;
int action;

void playerAction() {
    action = _getch();

    unsigned char maze[30][30];
    int prevposX = posX;
    int prevposY = posY;

    switch (action) {
    case KEY_LEFT: {
        if (maze[posX][posY - 1] != '|' && maze[posX][posY - 1] != '-' && maze[posX][posY - 1] != '+') {
            posY--;
        }

        maze[prevposX][prevposY] = ' ';
        system("cls");
        break;
    }

    case KEY_RIGHT: {
        if (maze[posX][posY + 1] != '|' && maze[posX][posY + 1] != '-' && maze[posX][posY + 1] != '+') {
            posY++;
        }

        maze[prevposX][prevposY] = ' ';
        system("cls");
        break;
    }

    case KEY_DOWN: {
        if (maze[posX + 1][posY] != '|' && maze[posX + 1][posY] != '+' && maze[posX + 1][posY] != '-') {
            posX++;
        }

        maze[prevposX][prevposY] = ' ';
        system("cls");
        break;
    }

    case KEY_UP: {
        if (maze[posX - 1][posY] != '|' && maze[posX - 1][posY] != '+' && maze[posX - 1][posY] != '-') {
            posX--;
        }

        maze[prevposX][prevposY] = ' ';
        system("cls");
        break;
    }


    default: {
        system("cls");
        break;
    }
    }

}

